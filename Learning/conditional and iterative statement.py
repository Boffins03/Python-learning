# if, elif and else
a = int(input("Enter first number:"))
b = int(input("Enter  second number:"))

if a > b:
    print(f"A-{a} is greater than B-{b}")
elif a < b:
    print(f"B-{b} is greater than A-{a}")
else:
    print(f"A-{a} and B-{b} are equal")

# for loop 
for a in range(b):
    print(a)

# while loop
while b > a:
    print(b)
    b =-1

